package transport

import "context"

// Transport определяет интерфейс для транспортного слоя приложения
// Транспортный слой отвечает за взаимодействие с внешним миром (HTTP, gRPC, и т.д.)
type Transport interface {
	// Init инициализирует транспортный слой
	// Выполняет настройку маршрутизаторов, middleware и других компонентов
	Init() error

	// Start запускает транспортный слой и начинает обработку входящих запросов
	// Принимает контекст для возможности graceful shutdown
	// Блокирует выполнение до получения сигнала завершения через контекст
	Start(context.Context, chan struct{}) error
}
