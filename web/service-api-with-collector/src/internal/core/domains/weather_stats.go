package domains

// WeatherStats представляет статистические данные о погоде за период
// Содержит агрегированную информацию по измерениям с метеостанции
type WeatherStats struct {
	// ReadingsCount - общее количество измерений за период
	// Позволяет оценить полноту данных (чем больше, тем статистика достовернее)
	ReadingsCount int `json:"readings_count"`

	// AvgTemperature - средняя температура за период в градусах Цельсия (°C)
	// Рассчитывается как сумма всех температур / количество измерений
	AvgTemperature float64 `json:"avg_temperature"`

	// MinTemperature - минимальная температура за период в градусах Цельсия (°C)
	// Наименьшее зафиксированное значение температуры
	MinTemperature float64 `json:"min_temperature"`

	// MaxTemperature - максимальная температура за период в градусах Цельсия (°C)
	// Наибольшее зафиксированное значение температуры
	MaxTemperature float64 `json:"max_temperature"`

	// AvgHumidity - средняя влажность за период в процентах (%)
	// Рассчитывается как сумма всех значений влажности / количество измерений
	// Диапазон значений: 0-100%
	AvgHumidity float64 `json:"avg_humidity"`

	// AvgPressure - среднее атмосферное давление за период в гектопаскалях (гПа)
	// Рассчитывается как сумма всех значений давления / количество измерений
	// Стандартное атмосферное давление: 1013.25 гПа
	AvgPressure float64 `json:"avg_pressure"`
}
